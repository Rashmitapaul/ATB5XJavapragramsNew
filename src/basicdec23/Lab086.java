public class Lab086 {
    public static void main(String[] args) {
        //Do While loop= initialization ->body -> condition -> increment/decrement

        // Do While loop syntax
        int i = 1;//initialization
        do{
            System.out.println(i);//body
            i++;//increment

        }while (i <= 10);//condition

    }
}
