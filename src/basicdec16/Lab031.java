package src.basicdec16;

public class Lab031 {
    public static void main(String[] args) {
        //Ternary operator--> It is based on conditions.
        //int a = condition ? if this is  true print do this : if false do this.

        int a = true ? 10:20;
        System.out.println(a);
        int a1 = 30>40 ? 10:20;
        System.out.println(a1);
        String str = 10>20 ? "10":"TWENTY";
        System.out.println(str);
        String raj_status = true ? "Yes" : "No";
        System.out.println(raj_status);
    }
}
